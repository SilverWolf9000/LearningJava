public class forloops {

	public static void main(String[] args) {
		// forloops - sec. 1 declare var; sec. 2 give condition for validity; sec. 3 change for each postexecution
		for(int i = 0; i < 5; i++) {
			// printf specifies specific value using %d for int and \n = new line
			System.out.printf("The value of i is %d\n", i);
		}
	}

}
